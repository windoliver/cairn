//! SDK emitter — writes Rust types into `crates/cairn-core/src/generated/`.
//!
//! Walks the `Document` IR and produces:
//!
//! * `mod.rs` — root re-exports.
//! * `verbs/mod.rs` — `VerbId` enum + capability/auth registry + capability list.
//! * `verbs/<id>.rs` (× 8) — per-verb `Args` and `Data` Rust types.
//! * `common/mod.rs` — shared types lifted from `common/*.json`.
//! * `errors/mod.rs` — `ErrorCode` enum.
//! * `envelope/mod.rs` — Request / Response / `SignedIntent` types.
//! * `status.rs`, `handshake.rs` — protocol prelude responses.
//!
//! Output bytes are deterministic per `Document` IR; snapshot tests in Phase 6
//! lock down the exact representation.

use std::collections::BTreeSet;
use std::path::PathBuf;

use super::fmt::RustWriter;
use super::ir::{
    Document, EnumDef, EnumVariant, PreludeDef, Prim, RecursiveEnumDef, RustType, StructDef,
    StructField, TaggedUnionDef, TaggedVariant, TypeName, UntaggedUnionDef, VerbDef, pascal_case,
};
use super::{CodegenError, GeneratedFile};

const HEADER: &str = "// @generated by cairn-codegen — DO NOT EDIT.\n";
const RUSTFMT_SKIP: &str = "#![cfg_attr(rustfmt, rustfmt_skip)]\n";
const ROOT: &str = "crates/cairn-core/src/generated";
const FILE_LINTS: &str = "#![allow(clippy::module_name_repetitions, clippy::large_enum_variant, clippy::missing_errors_doc, clippy::missing_panics_doc, clippy::doc_markdown, clippy::match_same_arms, clippy::cast_lossless, clippy::bool_to_int_with_if, missing_docs, dead_code)]\n";

/// Emit every SDK file declared by `doc`.
///
/// # Errors
/// Returns [`CodegenError::Emit`] when an IR construct cannot be rendered.
pub fn emit(doc: &Document) -> Result<Vec<GeneratedFile>, CodegenError> {
    let common_names: BTreeSet<String> = doc.common.keys().map(|t| t.0.clone()).collect();

    let mut out = Vec::new();
    out.push(emit_root_mod(doc));
    out.push(emit_verbs_mod(doc));
    for verb in &doc.verbs {
        out.push(emit_verb_file(verb, &common_names)?);
    }
    out.push(emit_common(doc, &common_names)?);
    out.push(emit_errors(doc));
    out.push(emit_envelope(doc, &common_names)?);
    for prelude in &doc.preludes {
        out.push(emit_prelude(prelude, &common_names)?);
    }
    Ok(out)
}

// ── Root re-exports ──────────────────────────────────────────────────────────

fn emit_root_mod(_doc: &Document) -> GeneratedFile {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line("//! Generated SDK surface for the cairn.mcp.v1 contract.");
    w.line("//!");
    w.line("//! Regenerate with `cargo run -p cairn-idl --bin cairn-codegen`.");
    w.blank();
    w.line("pub mod common;");
    w.line("pub mod envelope;");
    w.line("pub mod errors;");
    w.line("pub mod handshake;");
    w.line("pub mod status;");
    w.line("pub mod verbs;");
    GeneratedFile {
        path: PathBuf::from(ROOT).join("mod.rs"),
        bytes: w.finish().into_bytes(),
    }
}

// ── Verbs registry ───────────────────────────────────────────────────────────

fn emit_verbs_mod(doc: &Document) -> GeneratedFile {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line("//! Verb registry. The eight P0 verbs in IDL order.");
    w.blank();
    for verb in &doc.verbs {
        w.line(&format!("pub mod {};", verb.id));
    }
    w.blank();
    w.line("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]");
    w.line("#[non_exhaustive]");
    w.line("pub enum VerbId {");
    w.indent();
    for verb in &doc.verbs {
        w.line(&format!("{},", pascal_case(&verb.id)));
    }
    w.dedent();
    w.line("}");
    w.blank();
    w.line("impl VerbId {");
    w.indent();
    w.line("#[must_use]");
    w.line("pub const fn as_str(self) -> &'static str {");
    w.indent();
    w.line("match self {");
    w.indent();
    for verb in &doc.verbs {
        w.line(&format!(
            "Self::{} => \"{}\",",
            pascal_case(&verb.id),
            verb.id
        ));
    }
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();
    w.line("#[must_use]");
    w.line("pub const fn capability(self) -> Option<&'static str> {");
    w.indent();
    w.line("match self {");
    w.indent();
    for verb in &doc.verbs {
        match &verb.capability {
            Some(c) => w.line(&format!(
                "Self::{} => Some(\"{c}\"),",
                pascal_case(&verb.id)
            )),
            None => w.line(&format!("Self::{} => None,", pascal_case(&verb.id))),
        }
    }
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();
    w.line("#[must_use]");
    w.line("pub const fn auth(self) -> &'static str {");
    w.indent();
    w.line("match self {");
    w.indent();
    for verb in &doc.verbs {
        w.line(&format!(
            "Self::{} => \"{}\",",
            pascal_case(&verb.id),
            verb.auth.as_str()
        ));
    }
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();
    w.line("/// Every advertised capability string in declaration order.");
    w.line("pub const CAPABILITIES: &[&str] = &[");
    w.indent();
    for cap in &doc.capabilities {
        w.line(&format!("\"{cap}\","));
    }
    w.dedent();
    w.line("];");
    GeneratedFile {
        path: PathBuf::from(ROOT).join("verbs/mod.rs"),
        bytes: w.finish().into_bytes(),
    }
}

// ── Per-verb file ────────────────────────────────────────────────────────────

fn emit_verb_file(
    verb: &VerbDef,
    common_names: &BTreeSet<String>,
) -> Result<GeneratedFile, CodegenError> {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line(&format!(
        "//! Generated SDK types for the `{}` verb.",
        verb.id
    ));
    w.blank();
    w.line("use serde::{Deserialize, Serialize};");
    w.blank();
    // Sibling locally-defined types referenced from Args/Data (e.g. `Hit`).
    // Emit before Args/Data so source-order references resolve.
    for (name, ty) in &verb.local_types {
        let renamed = renamed_to(ty, name.clone());
        write_type_decl(&mut w, &renamed, common_names)?;
        w.blank();
    }
    let args_fallback = format!("{}Args", pascal_case(&verb.id));
    let data_fallback = format!("{}Data", pascal_case(&verb.id));
    write_named_type(&mut w, &verb.args, &args_fallback, common_names)?;
    w.blank();
    write_named_type(&mut w, &verb.data, &data_fallback, common_names)?;
    w.blank();
    let schema_path = format!(
        "../../../../cairn-mcp/src/generated/schemas/verbs/{}.json",
        verb.id
    );
    w.line(&format!(
        "pub const ARGS_SCHEMA: &[u8] = include_bytes!(\"{schema_path}\");"
    ));
    Ok(GeneratedFile {
        path: PathBuf::from(ROOT).join(format!("verbs/{}.rs", verb.id)),
        bytes: w.finish().into_bytes(),
    })
}

// ── Common types ─────────────────────────────────────────────────────────────

fn emit_common(
    doc: &Document,
    common_names: &BTreeSet<String>,
) -> Result<GeneratedFile, CodegenError> {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line("//! Common types shared across verbs (Ulid, Cursor, Identity, ...).");
    w.blank();
    w.line("use serde::{Deserialize, Serialize};");
    w.blank();

    // BTreeMap iterates in sorted key order — deterministic.
    for (name, ty) in &doc.common {
        emit_common_entry(&mut w, name, ty, common_names)?;
        w.blank();
    }

    Ok(GeneratedFile {
        path: PathBuf::from(ROOT).join("common/mod.rs"),
        bytes: w.finish().into_bytes(),
    })
}

/// Emit one entry of the `common/` namespace. Primitive entries become
/// `pub type Foo = Bar;` (or newtype if aliasable to a primitive); structured
/// entries dispatch through `write_type_decl`.
fn emit_common_entry(
    w: &mut RustWriter,
    name: &TypeName,
    ty: &RustType,
    common_names: &BTreeSet<String>,
) -> Result<(), CodegenError> {
    match ty {
        RustType::Primitive(p) => {
            // Common primitives are exposed as transparent newtype wrappers
            // so the IDL-declared identity (e.g. `Ulid`) survives in the
            // type system.
            w.line("#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]");
            w.line("#[serde(transparent)]");
            w.line(&format!("pub struct {}(pub {});", name.0, render_prim(*p)));
        }
        RustType::Json => {
            w.line(&format!("pub type {} = serde_json::Value;", name.0));
        }
        RustType::Map(inner) => {
            w.line(&format!(
                "pub type {} = std::collections::BTreeMap<String, {}>;",
                name.0,
                render_inline_type(inner, common_names)
            ));
        }
        RustType::Vec(inner) => {
            w.line(&format!(
                "pub type {} = Vec<{}>;",
                name.0,
                render_inline_type(inner, common_names)
            ));
        }
        RustType::Optional(inner) => {
            w.line(&format!(
                "pub type {} = Option<{}>;",
                name.0,
                render_inline_type(inner, common_names)
            ));
        }
        RustType::Ref(target) => {
            w.line(&format!(
                "pub type {} = {};",
                name.0,
                render_ref(target, common_names)
            ));
        }
        RustType::Struct(_)
        | RustType::Enum(_)
        | RustType::TaggedUnion(_)
        | RustType::UntaggedUnion(_)
        | RustType::Recursive(_) => {
            // Force the IR's nominal name onto a clone of the IR variant so
            // top-level emission preserves the common-namespace identifier.
            let aliased = renamed_to(ty, name.clone());
            write_type_decl(w, &aliased, common_names)?;
        }
    }
    Ok(())
}

/// Return a copy of `ty` with its nominal name swapped to `name` (only the
/// outer Struct/Enum/Union — inner names are preserved).
fn renamed_to(ty: &RustType, name: TypeName) -> RustType {
    match ty.clone() {
        RustType::Struct(mut s) => {
            s.name = name;
            RustType::Struct(s)
        }
        RustType::Enum(mut e) => {
            e.name = name;
            RustType::Enum(e)
        }
        RustType::TaggedUnion(mut t) => {
            t.name = name;
            RustType::TaggedUnion(t)
        }
        RustType::UntaggedUnion(mut u) => {
            u.name = name;
            RustType::UntaggedUnion(u)
        }
        RustType::Recursive(mut r) => {
            r.name = name;
            RustType::Recursive(r)
        }
        other => other,
    }
}

// ── Errors enum ──────────────────────────────────────────────────────────────

fn emit_errors(doc: &Document) -> GeneratedFile {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line("//! Errors enum lowered from errors/error.json.");
    w.blank();
    w.line("use serde::{Deserialize, Serialize};");
    w.blank();
    w.line("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]");
    // No `rename_all` — IDL error codes are PascalCase wire constants and the
    // generated variant idents are already byte-identical to the wire form, so
    // serde's default naming serialises/deserialises them without any rename.
    w.line("#[non_exhaustive]");
    w.line("pub enum ErrorCode {");
    w.indent();
    for code in &doc.error_codes {
        w.line(&format!("{},", pascal_case(&code.code)));
    }
    w.dedent();
    w.line("}");
    w.blank();
    w.line("impl ErrorCode {");
    w.indent();
    w.line("#[must_use]");
    w.line("pub const fn as_str(self) -> &'static str {");
    w.indent();
    w.line("match self {");
    w.indent();
    for code in &doc.error_codes {
        w.line(&format!(
            "Self::{} => \"{}\",",
            pascal_case(&code.code),
            code.code
        ));
    }
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    GeneratedFile {
        path: PathBuf::from(ROOT).join("errors/mod.rs"),
        bytes: w.finish().into_bytes(),
    }
}

