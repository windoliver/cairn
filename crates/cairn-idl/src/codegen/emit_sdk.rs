//! SDK emitter — writes Rust types into `crates/cairn-core/src/generated/`.
//!
//! Walks the `Document` IR and produces:
//!
//! * `mod.rs` — root re-exports.
//! * `verbs/mod.rs` — `VerbId` enum + capability/auth registry + capability list.
//! * `verbs/<id>.rs` (× 8) — per-verb `Args` and `Data` Rust types.
//! * `common/mod.rs` — shared types lifted from `common/*.json`.
//! * `errors/mod.rs` — `ErrorCode` enum.
//! * `envelope/mod.rs` — Request / Response / `SignedIntent` types.
//! * `status.rs`, `handshake.rs` — protocol prelude responses.
//!
//! Output bytes are deterministic per `Document` IR; snapshot tests in Phase 6
//! lock down the exact representation.

use std::collections::BTreeSet;
use std::path::PathBuf;

use super::fmt::RustWriter;
use super::ir::{
    Document, EnumDef, EnumVariant, PreludeDef, Prim, RecursiveEnumDef, RustType, StructDef,
    StructField, TaggedUnionDef, TaggedVariant, TypeName, UntaggedUnionDef, VerbDef, pascal_case,
};
use super::{CodegenError, GeneratedFile};

const HEADER: &str = "// @generated by cairn-codegen — DO NOT EDIT.\n";
const RUSTFMT_SKIP: &str = "#![cfg_attr(rustfmt, rustfmt_skip)]\n";
const ROOT: &str = "crates/cairn-core/src/generated";
const FILE_LINTS: &str = "#![allow(clippy::module_name_repetitions, clippy::large_enum_variant, clippy::missing_errors_doc, clippy::missing_panics_doc, clippy::doc_markdown, clippy::match_same_arms, clippy::cast_lossless, clippy::bool_to_int_with_if, missing_docs, dead_code)]\n";

/// Emit every SDK file declared by `doc`.
///
/// # Errors
/// Returns [`CodegenError::Emit`] when an IR construct cannot be rendered.
pub fn emit(doc: &Document) -> Result<Vec<GeneratedFile>, CodegenError> {
    let common_names: BTreeSet<String> = doc.common.keys().map(|t| t.0.clone()).collect();

    let mut out = Vec::new();
    out.push(emit_root_mod(doc));
    out.push(emit_verbs_mod(doc));
    for verb in &doc.verbs {
        out.push(emit_verb_file(verb, &common_names)?);
    }
    out.push(emit_common(doc, &common_names)?);
    out.push(emit_errors(doc));
    out.push(emit_envelope(doc, &common_names)?);
    for prelude in &doc.preludes {
        out.push(emit_prelude(prelude, &common_names)?);
    }
    Ok(out)
}

// ── Root re-exports ──────────────────────────────────────────────────────────

fn emit_root_mod(_doc: &Document) -> GeneratedFile {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line("//! Generated SDK surface for the cairn.mcp.v1 contract.");
    w.line("//!");
    w.line("//! Regenerate with `cargo run -p cairn-idl --bin cairn-codegen`.");
    w.blank();
    w.line("pub mod common;");
    w.line("pub mod envelope;");
    w.line("pub mod errors;");
    w.line("pub mod handshake;");
    w.line("pub mod status;");
    w.line("pub mod verbs;");
    GeneratedFile {
        path: PathBuf::from(ROOT).join("mod.rs"),
        bytes: w.finish().into_bytes(),
    }
}

// ── Verbs registry ───────────────────────────────────────────────────────────

fn emit_verbs_mod(doc: &Document) -> GeneratedFile {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line("//! Verb registry. The eight P0 verbs in IDL order.");
    w.blank();
    for verb in &doc.verbs {
        w.line(&format!("pub mod {};", verb.id));
    }
    w.blank();
    w.line("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]");
    w.line("#[non_exhaustive]");
    w.line("pub enum VerbId {");
    w.indent();
    for verb in &doc.verbs {
        w.line(&format!("{},", pascal_case(&verb.id)));
    }
    w.dedent();
    w.line("}");
    w.blank();
    w.line("impl VerbId {");
    w.indent();
    w.line("#[must_use]");
    w.line("pub const fn as_str(self) -> &'static str {");
    w.indent();
    w.line("match self {");
    w.indent();
    for verb in &doc.verbs {
        w.line(&format!(
            "Self::{} => \"{}\",",
            pascal_case(&verb.id),
            verb.id
        ));
    }
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();
    w.line("#[must_use]");
    w.line("pub const fn capability(self) -> Option<&'static str> {");
    w.indent();
    w.line("match self {");
    w.indent();
    for verb in &doc.verbs {
        match &verb.capability {
            Some(c) => w.line(&format!(
                "Self::{} => Some(\"{c}\"),",
                pascal_case(&verb.id)
            )),
            None => w.line(&format!("Self::{} => None,", pascal_case(&verb.id))),
        }
    }
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();
    w.line("#[must_use]");
    w.line("pub const fn auth(self) -> &'static str {");
    w.indent();
    w.line("match self {");
    w.indent();
    for verb in &doc.verbs {
        w.line(&format!(
            "Self::{} => \"{}\",",
            pascal_case(&verb.id),
            verb.auth.as_str()
        ));
    }
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();
    w.line("/// Every advertised capability string in declaration order.");
    w.line("pub const CAPABILITIES: &[&str] = &[");
    w.indent();
    for cap in &doc.capabilities {
        w.line(&format!("\"{cap}\","));
    }
    w.dedent();
    w.line("];");
    GeneratedFile {
        path: PathBuf::from(ROOT).join("verbs/mod.rs"),
        bytes: w.finish().into_bytes(),
    }
}

// ── Per-verb file ────────────────────────────────────────────────────────────

fn emit_verb_file(
    verb: &VerbDef,
    common_names: &BTreeSet<String>,
) -> Result<GeneratedFile, CodegenError> {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line(&format!(
        "//! Generated SDK types for the `{}` verb.",
        verb.id
    ));
    w.blank();
    w.line("use serde::{Deserialize, Serialize};");
    w.blank();
    // Sibling locally-defined types referenced from Args/Data (e.g. `Hit`).
    // Emit before Args/Data so source-order references resolve.
    for (name, ty) in &verb.local_types {
        let renamed = renamed_to(ty, name.clone());
        write_type_decl(&mut w, &renamed, common_names)?;
        w.blank();
    }
    let args_fallback = format!("{}Args", pascal_case(&verb.id));
    let data_fallback = format!("{}Data", pascal_case(&verb.id));
    write_named_type(&mut w, &verb.args, &args_fallback, common_names)?;
    w.blank();
    write_named_type(&mut w, &verb.data, &data_fallback, common_names)?;
    w.blank();
    let schema_path = format!(
        "../../../../cairn-mcp/src/generated/schemas/verbs/{}.json",
        verb.id
    );
    w.line(&format!(
        "pub const ARGS_SCHEMA: &[u8] = include_bytes!(\"{schema_path}\");"
    ));
    Ok(GeneratedFile {
        path: PathBuf::from(ROOT).join(format!("verbs/{}.rs", verb.id)),
        bytes: w.finish().into_bytes(),
    })
}

// ── Common types ─────────────────────────────────────────────────────────────

fn emit_common(
    doc: &Document,
    common_names: &BTreeSet<String>,
) -> Result<GeneratedFile, CodegenError> {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line("//! Common types shared across verbs (Ulid, Cursor, Identity, ...).");
    w.blank();
    w.line("use serde::{Deserialize, Serialize};");
    w.blank();

    // BTreeMap iterates in sorted key order — deterministic.
    for (name, ty) in &doc.common {
        emit_common_entry(&mut w, name, ty, common_names)?;
        w.blank();
    }

    Ok(GeneratedFile {
        path: PathBuf::from(ROOT).join("common/mod.rs"),
        bytes: w.finish().into_bytes(),
    })
}

/// Emit one entry of the `common/` namespace. Primitive entries become
/// `pub type Foo = Bar;` (or newtype if aliasable to a primitive); structured
/// entries dispatch through `write_type_decl`.
fn emit_common_entry(
    w: &mut RustWriter,
    name: &TypeName,
    ty: &RustType,
    common_names: &BTreeSet<String>,
) -> Result<(), CodegenError> {
    match ty {
        RustType::Primitive(p) => {
            // Common primitives are exposed as transparent newtype wrappers
            // so the IDL-declared identity (e.g. `Ulid`) survives in the
            // type system. For string newtypes that carry a pattern in the
            // IDL (Ulid, Cursor, …) we hand-roll Deserialize so the pattern
            // is enforced at every call site, not only at the SignedIntent
            // envelope. Serialize stays transparent.
            if matches!(p, Prim::String) && primitive_has_pattern(&name.0) {
                w.line("#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize)]");
                w.line("#[serde(transparent)]");
                w.line(&format!("pub struct {}(pub {});", name.0, render_prim(*p)));
                w.blank();
                write_pattern_newtype_deserialize(w, &name.0);
            } else {
                w.line("#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]");
                w.line("#[serde(transparent)]");
                w.line(&format!("pub struct {}(pub {});", name.0, render_prim(*p)));
            }
        }
        RustType::Json => {
            w.line(&format!("pub type {} = serde_json::Value;", name.0));
        }
        RustType::Map(inner) => {
            w.line(&format!(
                "pub type {} = std::collections::BTreeMap<String, {}>;",
                name.0,
                render_inline_type(inner, common_names)
            ));
        }
        RustType::Vec(inner) => {
            w.line(&format!(
                "pub type {} = Vec<{}>;",
                name.0,
                render_inline_type(inner, common_names)
            ));
        }
        RustType::Optional(inner) => {
            w.line(&format!(
                "pub type {} = Option<{}>;",
                name.0,
                render_inline_type(inner, common_names)
            ));
        }
        RustType::Ref(target) => {
            w.line(&format!(
                "pub type {} = {};",
                name.0,
                render_ref(target, common_names)
            ));
        }
        RustType::Struct(_)
        | RustType::Enum(_)
        | RustType::TaggedUnion(_)
        | RustType::UntaggedUnion(_)
        | RustType::Recursive(_) => {
            // Force the IR's nominal name onto a clone of the IR variant so
            // top-level emission preserves the common-namespace identifier.
            let aliased = renamed_to(ty, name.clone());
            write_type_decl(w, &aliased, common_names)?;
        }
    }
    Ok(())
}

/// String-newtype types whose IDL definition carries a `pattern` (or other
/// shape constraint) that must be enforced at every deserialise boundary.
/// Listed by Rust type name. The default `#[serde(transparent)]` Deserialize
/// derive accepts any string — this allow-list opts the named newtypes into
/// a hand-rolled Deserialize that runs the shape check first.
fn primitive_has_pattern(name: &str) -> bool {
    matches!(name, "Ulid" | "Cursor")
}

/// Emit a hand-rolled `Deserialize` for a string newtype whose value must
/// satisfy a pattern / length cap declared in the IDL. The body dispatches by
/// the newtype's nominal name — adding a new pattern-bearing newtype means
/// extending [`primitive_has_pattern`] and this match.
fn write_pattern_newtype_deserialize(w: &mut RustWriter, name: &str) {
    w.line(&format!(
        "impl<'de> ::serde::Deserialize<'de> for {name} {{"
    ));
    w.indent();
    w.line("fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>");
    w.line("where D: ::serde::Deserializer<'de> {");
    w.indent();
    w.line("let s = <String as ::serde::Deserialize>::deserialize(deserializer)?;");
    match name {
        "Ulid" => {
            // Crockford base32, 26 chars, alphabet [0-9A-HJKMNP-TV-Z].
            // Error messages keep the uppercase "ULID" tag so callers
            // grepping for the IDL primitive name see a consistent token
            // across every call site (SignedIntent's bespoke check uses the
            // same wording — see write_signed_intent_extra_checks).
            w.line("if s.len() != 26 { return Err(::serde::de::Error::custom(\"ULID: must be 26 chars\")); }");
            w.line("if !s.bytes().all(|b| matches!(b, b'0'..=b'9' | b'A'..=b'H' | b'J' | b'K' | b'M' | b'N' | b'P'..=b'T' | b'V'..=b'Z')) {");
            w.indent();
            w.line("return Err(::serde::de::Error::custom(\"ULID: must be Crockford base32 (uppercase, no I/L/O/U)\"));");
            w.dedent();
            w.line("}");
        }
        "Cursor" => {
            // Opaque token: minLength: 1, maxLength: 512.
            w.line("if s.is_empty() { return Err(::serde::de::Error::custom(\"Cursor: must not be empty\")); }");
            w.line("if s.len() > 512 { return Err(::serde::de::Error::custom(\"Cursor: must be <= 512 chars\")); }");
        }
        _ => {
            // Catch-all so adding a new entry to `primitive_has_pattern`
            // without updating this match is a compile-time tap on the
            // shoulder rather than a silent passthrough.
            w.line(&format!(
                "let _ = &s; return Err(::serde::de::Error::custom(\"{name}: pattern unimplemented\"));"
            ));
        }
    }
    w.line(&format!("Ok({name}(s))"));
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
}

/// Return a copy of `ty` with its nominal name swapped to `name` (only the
/// outer Struct/Enum/Union — inner names are preserved).
fn renamed_to(ty: &RustType, name: TypeName) -> RustType {
    match ty.clone() {
        RustType::Struct(mut s) => {
            s.name = name;
            RustType::Struct(s)
        }
        RustType::Enum(mut e) => {
            e.name = name;
            RustType::Enum(e)
        }
        RustType::TaggedUnion(mut t) => {
            t.name = name;
            RustType::TaggedUnion(t)
        }
        RustType::UntaggedUnion(mut u) => {
            u.name = name;
            RustType::UntaggedUnion(u)
        }
        RustType::Recursive(mut r) => {
            r.name = name;
            RustType::Recursive(r)
        }
        other => other,
    }
}

// ── Errors enum ──────────────────────────────────────────────────────────────

fn emit_errors(doc: &Document) -> GeneratedFile {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line("//! Errors enum lowered from errors/error.json.");
    w.blank();
    w.line("use serde::{Deserialize, Serialize};");
    w.blank();
    w.line("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]");
    // No `rename_all` — IDL error codes are PascalCase wire constants and the
    // generated variant idents are already byte-identical to the wire form, so
    // serde's default naming serialises/deserialises them without any rename.
    w.line("#[non_exhaustive]");
    w.line("pub enum ErrorCode {");
    w.indent();
    for code in &doc.error_codes {
        w.line(&format!("{},", pascal_case(&code.code)));
    }
    w.dedent();
    w.line("}");
    w.blank();
    w.line("impl ErrorCode {");
    w.indent();
    w.line("#[must_use]");
    w.line("pub const fn as_str(self) -> &'static str {");
    w.indent();
    w.line("match self {");
    w.indent();
    for code in &doc.error_codes {
        w.line(&format!(
            "Self::{} => \"{}\",",
            pascal_case(&code.code),
            code.code
        ));
    }
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    GeneratedFile {
        path: PathBuf::from(ROOT).join("errors/mod.rs"),
        bytes: w.finish().into_bytes(),
    }
}

// ── Envelope (request, response, signed_intent) ──────────────────────────────

fn emit_envelope(
    doc: &Document,
    common_names: &BTreeSet<String>,
) -> Result<GeneratedFile, CodegenError> {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line("//! Request / response envelope types.");
    w.blank();
    w.line("use serde::{Deserialize, Serialize};");
    w.blank();
    for (name, ty) in &doc.envelope {
        if name.0 == "Request" {
            write_request_envelope(&mut w, doc, ty, common_names)?;
        } else if name.0 == "Response" {
            write_response_envelope(&mut w, doc, ty, common_names)?;
        } else {
            write_type_decl(&mut w, ty, common_names)?;
        }
        w.blank();
    }
    // SignedIntent's bespoke TryFrom uses `is_ulid_shape`. Emit the helper
    // once, at module scope, so any TryFrom validation can reference it.
    write_ulid_shape_helper(&mut w);
    w.blank();
    write_error_envelope_validator(&mut w, doc);
    Ok(GeneratedFile {
        path: PathBuf::from(ROOT).join("envelope/mod.rs"),
        bytes: w.finish().into_bytes(),
    })
}

/// Emit `validate_error_envelope`, used by the Response Deserialize impl to
/// enforce the error.json shape contract on every present `error` payload:
/// `code` is in the closed enum, `message` is a non-empty string, and for
/// codes whose IDL `data` schema requires named fields, those fields are
/// present and (where the schema marks them strings) non-empty.
///
/// The per-code data requirements are enumerated below — they mirror
/// `errors/error.json` and would be lifted into a typed Error envelope in #62.
fn write_error_envelope_validator(w: &mut RustWriter, doc: &Document) {
    w.line("/// Structural validator for the error envelope payload. Mirrors the");
    w.line("/// closed `oneOf` in `errors/error.json` until the typed Error envelope");
    w.line("/// lands (#62). See `write_error_envelope_validator` in emit_sdk.rs for");
    w.line("/// the per-code data requirements.");
    w.line("#[allow(clippy::result_unit_err)]");
    w.line("fn validate_error_envelope(err: &::serde_json::Value) -> Result<(), &'static str> {");
    w.indent();
    w.line("let obj = err.as_object().ok_or(\"error: must be a JSON object\")?;");
    w.line(
        "let code = obj.get(\"code\").and_then(::serde_json::Value::as_str).ok_or(\"error: code must be a string\")?;",
    );
    w.line("match code {");
    w.indent();
    let known: Vec<&str> = doc.error_codes.iter().map(|e| e.code.as_str()).collect();
    let alts = known
        .iter()
        .map(|c| format!("\"{c}\""))
        .collect::<Vec<_>>()
        .join(" | ");
    w.line(&format!("{alts} => {{}},"));
    w.line("_ => return Err(\"error: code not in closed enum\"),");
    w.dedent();
    w.line("}");
    // Message: required + minLength: 1 on every variant.
    w.line(
        "let message = obj.get(\"message\").and_then(::serde_json::Value::as_str).ok_or(\"error: message must be a string\")?;",
    );
    w.line("if message.is_empty() { return Err(\"error: message must not be empty\"); }");
    // Per-code data requirements. Codes without required `data` (Internal,
    // MissingSignature) are tolerated — the IDL marks `data` optional or its
    // sub-schema empty.
    w.line("match code {");
    w.indent();
    // Each arm: required field name(s) + value-shape predicate(s).
    // Strings are checked non-empty, integers checked >= 0 / >= 1.
    write_error_data_arm(
        w,
        "InvalidArgs",
        &[
            ("field", FieldShape::NonEmptyString),
            ("reason", FieldShape::NonEmptyString),
        ],
    );
    write_error_data_arm(
        w,
        "InvalidFilter",
        &[("reason", FieldShape::NonEmptyString)],
    );
    write_error_data_arm(
        w,
        "CapabilityUnavailable",
        &[("capability", FieldShape::NonEmptyString)],
    );
    write_error_data_arm(w, "UnknownVerb", &[("verb", FieldShape::NonEmptyString)]);
    write_error_data_arm(
        w,
        "ExpiredIntent",
        &[
            ("issued_at", FieldShape::NonEmptyString),
            ("expires_at", FieldShape::NonEmptyString),
            ("now", FieldShape::NonEmptyString),
        ],
    );
    write_error_data_arm(
        w,
        "ReplayDetected",
        &[("operation_id", FieldShape::NonEmptyString)],
    );
    write_error_data_arm(
        w,
        "OutOfOrderSequence",
        &[
            ("issuer", FieldShape::NonEmptyString),
            ("high_water", FieldShape::NonNegativeInt),
            ("attempted", FieldShape::NonNegativeInt),
        ],
    );
    write_error_data_arm(
        w,
        "RevokedKey",
        &[
            ("issuer", FieldShape::NonEmptyString),
            ("key_version", FieldShape::PositiveInt),
        ],
    );
    write_error_data_arm(
        w,
        "Unauthorized",
        &[("required", FieldShape::NonEmptyString)],
    );
    write_error_data_arm(w, "NotFound", &[("target", FieldShape::NonEmptyString)]);
    write_error_data_arm(
        w,
        "ConflictVersion",
        &[
            ("expected", FieldShape::NonNegativeInt),
            ("actual", FieldShape::NonNegativeInt),
        ],
    );
    write_error_data_arm(
        w,
        "QuarantineRequired",
        &[("reason", FieldShape::NonEmptyString)],
    );
    write_error_data_arm(
        w,
        "PluginSuspended",
        &[("plugin_id", FieldShape::NonEmptyString)],
    );
    // Internal and MissingSignature have no required data fields.
    w.line("\"Internal\" | \"MissingSignature\" => {},");
    w.line("_ => {},");
    w.dedent();
    w.line("}");
    w.line("Ok(())");
    w.dedent();
    w.line("}");
}

#[derive(Clone, Copy)]
enum FieldShape {
    NonEmptyString,
    NonNegativeInt,
    PositiveInt,
}

fn write_error_data_arm(w: &mut RustWriter, code: &str, fields: &[(&str, FieldShape)]) {
    w.line(&format!("\"{code}\" => {{"));
    w.indent();
    w.line(&format!(
        "let data = obj.get(\"data\").and_then(::serde_json::Value::as_object).ok_or(\"error.code={code}: data object required\")?;"
    ));
    for (name, shape) in fields {
        match shape {
            FieldShape::NonEmptyString => {
                w.line(&format!(
                    "let v = data.get(\"{name}\").and_then(::serde_json::Value::as_str).ok_or(\"error.code={code}: data.{name} must be a string\")?;"
                ));
                w.line(&format!(
                    "if v.is_empty() {{ return Err(\"error.code={code}: data.{name} must not be empty\"); }}"
                ));
            }
            FieldShape::NonNegativeInt => {
                w.line(&format!(
                    "let v = data.get(\"{name}\").and_then(::serde_json::Value::as_i64).ok_or(\"error.code={code}: data.{name} must be a non-negative integer\")?;"
                ));
                w.line(&format!(
                    "if v < 0 {{ return Err(\"error.code={code}: data.{name} must be a non-negative integer\"); }}"
                ));
            }
            FieldShape::PositiveInt => {
                w.line(&format!(
                    "let v = data.get(\"{name}\").and_then(::serde_json::Value::as_i64).ok_or(\"error.code={code}: data.{name} must be a positive integer\")?;"
                ));
                w.line(&format!(
                    "if v < 1 {{ return Err(\"error.code={code}: data.{name} must be a positive integer\"); }}"
                ));
            }
        }
    }
    w.dedent();
    w.line("},");
}

/// Bespoke emitter for the `Request` envelope.
///
/// The IDL request schema declares `args: object|array` and binds the
/// concrete shape via `allOf` if/then arms keyed off `verb`. The default
/// struct lowering drops those arms and types `args` as
/// `serde_json::Value`, so any wrong-shape payload would silently round-trip.
///
/// To restore the contract on the wire we emit:
///   * a typed `RequestArgs` enum (one variant per verb, holding the verb's
///     concrete `Args` type),
///   * the public `Request` struct using `RequestArgs` (Serialize-only),
///   * a hand-rolled `Deserialize` that goes through a `RawRequest` shim,
///     enforces the `contract` literal, then dispatches `args` by `verb`.
#[allow(
    clippy::too_many_lines,
    reason = "single-pass emission keeps the bespoke envelope dispatch in one place"
)]
fn write_request_envelope(
    w: &mut RustWriter,
    doc: &Document,
    ty: &RustType,
    common: &BTreeSet<String>,
) -> Result<(), CodegenError> {
    // Pull the field list out of the lowered Request struct so any future
    // schema additions (signed_intent, contract, ...) flow through without a
    // bespoke field list to maintain.
    let RustType::Struct(s) = ty else {
        return Err(CodegenError::Emit(
            "envelope/Request expected to lower to a Struct".to_string(),
        ));
    };

    // Emit nested structured types in the fields first (e.g. the `RequestVerb`
    // enum hoisted from the `verb` field). Same convention `write_struct` uses.
    let mut nested = Vec::new();
    collect_nested_from_fields(&s.fields, "Request", &mut nested);
    for (sub_name, sub_ty) in &nested {
        let renamed = renamed_to(sub_ty, TypeName::new(sub_name));
        write_type_decl(w, &renamed, common)?;
        w.blank();
    }

    // Variants enumerate every verb in IDL order — same order the IR carries.
    // Each variant wraps `crate::generated::verbs::<id>::<Pascal>Args`.
    w.line("/// Per-verb request payload, dispatched on `Request.verb` at deserialize time.");
    w.line("#[derive(Debug, Clone, PartialEq, Serialize)]");
    w.line("#[serde(untagged)]");
    w.line("#[non_exhaustive]");
    w.line("pub enum RequestArgs {");
    w.indent();
    for verb in &doc.verbs {
        let pascal = pascal_case(&verb.id);
        w.line(&format!(
            "{pascal}(crate::generated::verbs::{}::{pascal}Args),",
            verb.id
        ));
    }
    w.dedent();
    w.line("}");
    w.blank();

    // Public struct — Serialize only; Deserialize is hand-rolled below.
    if let Some(doc_str) = &s.doc {
        write_doc(w, doc_str);
    }
    w.line("#[derive(Debug, Clone, PartialEq, Serialize)]");
    if s.deny_unknown_fields {
        w.line("#[serde(deny_unknown_fields)]");
    }
    w.line("pub struct Request {");
    w.indent();
    for field in &s.fields {
        if field.name == "args" {
            // Override `args` to the typed enum.
            if let Some(d) = &field.doc {
                write_doc(w, d);
            }
            w.line("pub args: RequestArgs,");
        } else {
            write_field(w, field, "Request", common, true);
        }
    }
    w.dedent();
    w.line("}");
    w.blank();

    // Private raw shim — same fields as the public struct, but `args` is an
    // opaque `serde_json::Value` so the dispatch happens after the verb has
    // been parsed.
    w.line("#[derive(Deserialize)]");
    w.line("#[serde(deny_unknown_fields)]");
    w.line("struct RawRequest {");
    w.indent();
    for field in &s.fields {
        if field.name == "args" {
            w.line("args: serde_json::Value,");
        } else {
            // Raw shim is Deserialize-only; pass `serializing = false`.
            write_field_inner(w, field, "Request", common, false, false);
        }
    }
    w.dedent();
    w.line("}");
    w.blank();

    // Hand-rolled Deserialize: parse the shim, enforce contract literal,
    // then dispatch args by verb.
    w.line("impl<'de> ::serde::Deserialize<'de> for Request {");
    w.indent();
    w.line("fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>");
    w.line("where D: ::serde::Deserializer<'de> {");
    w.indent();
    w.line("let raw = RawRequest::deserialize(deserializer)?;");
    w.line(&format!("if raw.contract != \"{}\" {{", doc.contract));
    w.indent();
    w.line(&format!(
        "return Err(::serde::de::Error::custom(format!(\"contract: expected \\\"{}\\\", got {{:?}}\", raw.contract)));",
        doc.contract
    ));
    w.dedent();
    w.line("}");
    w.line("let args = match raw.verb {");
    w.indent();
    for verb in &doc.verbs {
        let pascal = pascal_case(&verb.id);
        w.line(&format!("RequestVerb::{pascal} => RequestArgs::{pascal}("));
        w.indent();
        w.line(&format!(
            "<crate::generated::verbs::{}::{pascal}Args as ::serde::Deserialize>::deserialize(raw.args).map_err(::serde::de::Error::custom)?",
            verb.id
        ));
        w.dedent();
        w.line("),");
    }
    w.dedent();
    w.line("};");
    w.line("Ok(Self {");
    w.indent();
    for field in &s.fields {
        let ident = sanitise_ident(&field.name);
        if field.name == "args" {
            w.line("args,");
        } else {
            w.line(&format!("{ident}: raw.{ident},"));
        }
    }
    w.dedent();
    w.line("})");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    Ok(())
}

/// Bespoke emitter for the `Response` envelope.
///
/// The IDL response schema declares `data: object|array` and binds the
/// concrete shape per-verb via allOf if/then arms, plus four cross-cutting
/// invariants:
///   * status=committed ⟹ data present, error absent
///   * status∈{rejected,aborted} ⟹ error present, data absent
///   * verb=retrieve + status=committed ⟹ target present
///   * verb≠retrieve ⟹ target absent
///
/// Plus the bidirectional UnknownVerb binding from response.json:
///   * verb=unknown ⟹ status=rejected AND error.code=UnknownVerb
///   * status=rejected AND error.code=UnknownVerb ⟹ verb=unknown
///
/// And per-target retrieve dispatch — when `verb=retrieve` and
/// `status=committed`, `data` MUST match the per-target Data sub-shape
/// declared by `verbs/retrieve.json` (DataRecord, DataSession, …).
///
/// The default Struct lowering drops every arm, so a malformed response
/// silently round-trips. We dispatch `data` by verb (typed), retrieve by
/// target, and enforce the cross-cutting invariants in a hand-rolled
/// Deserialize.
#[allow(
    clippy::too_many_lines,
    reason = "single-pass emission keeps the bespoke envelope dispatch in one place"
)]
fn write_response_envelope(
    w: &mut RustWriter,
    doc: &Document,
    ty: &RustType,
    common: &BTreeSet<String>,
) -> Result<(), CodegenError> {
    let RustType::Struct(s) = ty else {
        return Err(CodegenError::Emit(
            "envelope/Response expected to lower to a Struct".to_string(),
        ));
    };

    // Hoist nested types (ResponseStatus, ResponseTarget, ResponseVerb,
    // ResponsePolicyTrace, ...) the same way `write_struct` does.
    let mut nested = Vec::new();
    collect_nested_from_fields(&s.fields, "Response", &mut nested);
    for (sub_name, sub_ty) in &nested {
        let renamed = renamed_to(sub_ty, TypeName::new(sub_name));
        write_type_decl(w, &renamed, common)?;
        w.blank();
    }

    // Per-target typed dispatch for retrieve responses. Each variant wraps
    // the matching Data sub-type from `verbs/retrieve.rs`. Untagged because
    // the discriminator (`target`) lives on the response envelope, not in
    // the data payload itself.
    w.line("/// Retrieve response payload, dispatched on `Response.target` at deserialize time.");
    w.line("#[derive(Debug, Clone, PartialEq, Serialize)]");
    w.line("#[serde(untagged)]");
    w.line("#[non_exhaustive]");
    w.line("pub enum RetrieveData {");
    w.indent();
    w.line("Record(crate::generated::verbs::retrieve::DataRecord),");
    w.line("Profile(crate::generated::verbs::retrieve::DataProfile),");
    w.line("Session(crate::generated::verbs::retrieve::DataSession),");
    w.line("Turn(crate::generated::verbs::retrieve::DataTurn),");
    w.line("Folder(crate::generated::verbs::retrieve::DataFolder),");
    w.line("Scope(crate::generated::verbs::retrieve::DataScope),");
    w.dedent();
    w.line("}");
    w.blank();

    // Typed per-verb data dispatch. Each variant wraps the verb's Data type
    // (`crate::generated::verbs::<id>::<Pascal>Data`). Retrieve wraps the
    // target-dispatched `RetrieveData` enum above. The `unknown` verb
    // (UnknownVerb sentinel) carries no data — there is no `verbs/unknown.rs`.
    w.line("/// Per-verb response payload, dispatched on `Response.verb` at deserialize time.");
    w.line("/// Retrieve sub-dispatches on `Response.target` to a typed `RetrieveData`.");
    w.line("#[derive(Debug, Clone, PartialEq, Serialize)]");
    w.line("#[serde(untagged)]");
    w.line("#[non_exhaustive]");
    w.line("pub enum ResponseData {");
    w.indent();
    for verb in &doc.verbs {
        let pascal = pascal_case(&verb.id);
        if verb.id == "retrieve" {
            w.line(&format!("{pascal}(RetrieveData),"));
        } else {
            w.line(&format!(
                "{pascal}(crate::generated::verbs::{}::{pascal}Data),",
                verb.id
            ));
        }
    }
    w.dedent();
    w.line("}");
    w.blank();

    // Public struct — Serialize-derived, Deserialize hand-rolled below.
    if let Some(doc_str) = &s.doc {
        write_doc(w, doc_str);
    }
    w.line("#[derive(Debug, Clone, PartialEq, Serialize)]");
    if s.deny_unknown_fields {
        w.line("#[serde(deny_unknown_fields)]");
    }
    w.line("pub struct Response {");
    w.indent();
    for field in &s.fields {
        if field.name == "data" {
            if let Some(d) = &field.doc {
                write_doc(w, d);
            }
            w.line("#[serde(default, skip_serializing_if = \"Option::is_none\")]");
            w.line("pub data: Option<ResponseData>,");
        } else {
            write_field(w, field, "Response", common, true);
        }
    }
    w.dedent();
    w.line("}");
    w.blank();

    // Private raw shim mirrors the public struct but keeps `data` as
    // serde_json::Value so dispatch happens after `verb` and the four
    // structural invariants are checked.
    w.line("#[derive(Deserialize)]");
    w.line("#[serde(deny_unknown_fields)]");
    w.line("struct RawResponse {");
    w.indent();
    for field in &s.fields {
        if field.name == "data" {
            w.line("#[serde(default)]");
            w.line("data: Option<serde_json::Value>,");
        } else {
            write_field_inner(w, field, "Response", common, false, false);
        }
    }
    w.dedent();
    w.line("}");
    w.blank();

    // Hand-rolled Deserialize: contract literal, status/data/error/target
    // invariants, then per-verb data dispatch.
    w.line("impl<'de> ::serde::Deserialize<'de> for Response {");
    w.indent();
    w.line("fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>");
    w.line("where D: ::serde::Deserializer<'de> {");
    w.indent();
    w.line("let raw = RawResponse::deserialize(deserializer)?;");
    w.line(&format!("if raw.contract != \"{}\" {{", doc.contract));
    w.indent();
    w.line(&format!(
        "return Err(::serde::de::Error::custom(format!(\"contract: expected \\\"{}\\\", got {{:?}}\", raw.contract)));",
        doc.contract
    ));
    w.dedent();
    w.line("}");
    // Invariant: committed ⟹ data present, error absent.
    w.line("if matches!(raw.status, ResponseStatus::Committed) {");
    w.indent();
    w.line(
        "if raw.data.is_none() { return Err(::serde::de::Error::custom(\"status=committed requires data\")); }",
    );
    w.line(
        "if raw.error.is_some() { return Err(::serde::de::Error::custom(\"status=committed forbids error\")); }",
    );
    w.dedent();
    w.line("}");
    // Invariant: rejected/aborted ⟹ error present, data absent.
    w.line("if matches!(raw.status, ResponseStatus::Rejected | ResponseStatus::Aborted) {");
    w.indent();
    w.line(
        "if raw.error.is_none() { return Err(::serde::de::Error::custom(\"status=rejected|aborted requires error\")); }",
    );
    w.line(
        "if raw.data.is_some() { return Err(::serde::de::Error::custom(\"status=rejected|aborted forbids data\")); }",
    );
    w.dedent();
    w.line("}");
    // Invariant: verb=retrieve + committed ⟹ target present.
    w.line(
        "if matches!(raw.verb, ResponseVerb::Retrieve) && matches!(raw.status, ResponseStatus::Committed) && raw.target.is_none() {",
    );
    w.indent();
    w.line(
        "return Err(::serde::de::Error::custom(\"verb=retrieve + status=committed requires target\"));",
    );
    w.dedent();
    w.line("}");
    // Invariant: verb≠retrieve ⟹ target absent.
    w.line("if !matches!(raw.verb, ResponseVerb::Retrieve) && raw.target.is_some() {");
    w.indent();
    w.line(
        "return Err(::serde::de::Error::custom(\"target is retrieve-only — forbidden for other verbs\"));",
    );
    w.dedent();
    w.line("}");
    // UnknownVerb bidirectional binding — verb=unknown ⟺ status=rejected
    // AND error.code=UnknownVerb. The error envelope is currently typed as
    // `serde_json::Value` (the IDL Error oneOf hasn't been lowered yet —
    // tracked by #62), so we read `error.code` defensively from JSON. When
    // the typed Error lands the same checks port without changing semantics.
    w.line("let error_code = raw.error.as_ref().and_then(|e| e.get(\"code\")).and_then(|c| c.as_str());");
    w.line("if matches!(raw.verb, ResponseVerb::Unknown) {");
    w.indent();
    w.line(
        "if !matches!(raw.status, ResponseStatus::Rejected) { return Err(::serde::de::Error::custom(\"verb=unknown requires status=rejected\")); }",
    );
    w.line(
        "if error_code != Some(\"UnknownVerb\") { return Err(::serde::de::Error::custom(\"verb=unknown requires error.code=UnknownVerb\")); }",
    );
    w.dedent();
    w.line("}");
    w.line(
        "if !matches!(raw.verb, ResponseVerb::Unknown) && error_code == Some(\"UnknownVerb\") {",
    );
    w.indent();
    w.line(
        "return Err(::serde::de::Error::custom(\"error.code=UnknownVerb is paired with verb=unknown only\"));",
    );
    w.dedent();
    w.line("}");
    // Bind the per-status code family from response.json. A status=rejected
    // response carrying an aborted-family code (or vice versa) is a real wire
    // bug — downstream callers branch on family/status, not just code.
    // Validate against the closed lists lifted from
    // `envelope/response.json#x-cairn-error-code-families`.
    w.line("if let Some(code) = error_code {");
    w.indent();
    w.line("match raw.status {");
    w.indent();
    let rejected_alts = doc
        .rejected_error_codes
        .iter()
        .map(|c| format!("\"{c}\""))
        .collect::<Vec<_>>()
        .join(" | ");
    w.line("ResponseStatus::Rejected => {");
    w.indent();
    w.line(&format!("if !matches!(code, {rejected_alts}) {{"));
    w.indent();
    w.line(
        "return Err(::serde::de::Error::custom(format!(\"status=rejected: error.code={code:?} not in rejected family\")));",
    );
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("},");
    let aborted_alts = doc
        .aborted_error_codes
        .iter()
        .map(|c| format!("\"{c}\""))
        .collect::<Vec<_>>()
        .join(" | ");
    w.line("ResponseStatus::Aborted => {");
    w.indent();
    w.line(&format!("if !matches!(code, {aborted_alts}) {{"));
    w.indent();
    w.line(
        "return Err(::serde::de::Error::custom(format!(\"status=aborted: error.code={code:?} not in aborted family\")));",
    );
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("},");
    w.line("ResponseStatus::Committed => {},");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    // Generic error envelope shape per errors/error.json (until the typed
    // Error envelope ships in #62). For every present error we enforce:
    //   * `code` is one of the closed enum (rejecting typo'd / synthetic codes
    //     that callers might smuggle past dispatch),
    //   * `message` is a non-empty string (the IDL marks `message` required +
    //     `minLength: 1` on every variant),
    //   * for variants whose IDL `data` schema requires a field, that field is
    //     present and non-empty.
    // The set of valid codes and their per-variant data requirements is
    // enumerated in `validate_error_envelope` below.
    w.line("if let Some(err) = raw.error.as_ref() {");
    w.indent();
    w.line("validate_error_envelope(err).map_err(::serde::de::Error::custom)?;");
    w.dedent();
    w.line("}");
    // Per-verb data dispatch. Skipped on rejected/aborted (data was just
    // proven None above).
    w.line("let data = if let Some(payload) = raw.data {");
    w.indent();
    w.line("Some(match raw.verb {");
    w.indent();
    for verb in &doc.verbs {
        let pascal = pascal_case(&verb.id);
        if verb.id == "retrieve" {
            // Retrieve dispatches by `target` — the IDL response schema's
            // per-target if/then arms bind `data` to the matching Data
            // sub-type (DataRecord / DataSession / ...). The structural check
            // above already proved `target` is present on committed retrieve
            // responses; match on it here to land in the typed RetrieveData
            // variant. The structural rule that retrieve+committed ⟹ target
            // also holds the inverse (target absent here means status was
            // not committed, which means data was already None per the
            // committed-only-data invariant). Defensively reject any payload
            // that arrives without a target.
            w.line(&format!("ResponseVerb::{pascal} => {{",));
            w.indent();
            w.line(
                "let target = raw.target.ok_or_else(|| ::serde::de::Error::custom(\"verb=retrieve with data requires target\"))?;",
            );
            w.line(&format!("ResponseData::{pascal}(match target {{"));
            w.indent();
            w.line("ResponseTarget::Record => RetrieveData::Record(");
            w.indent();
            w.line("<crate::generated::verbs::retrieve::DataRecord as ::serde::Deserialize>::deserialize(payload).map_err(::serde::de::Error::custom)?");
            w.dedent();
            w.line("),");
            w.line("ResponseTarget::Profile => RetrieveData::Profile(");
            w.indent();
            w.line("<crate::generated::verbs::retrieve::DataProfile as ::serde::Deserialize>::deserialize(payload).map_err(::serde::de::Error::custom)?");
            w.dedent();
            w.line("),");
            w.line("ResponseTarget::Session => RetrieveData::Session(");
            w.indent();
            w.line("<crate::generated::verbs::retrieve::DataSession as ::serde::Deserialize>::deserialize(payload).map_err(::serde::de::Error::custom)?");
            w.dedent();
            w.line("),");
            w.line("ResponseTarget::Turn => RetrieveData::Turn(");
            w.indent();
            w.line("<crate::generated::verbs::retrieve::DataTurn as ::serde::Deserialize>::deserialize(payload).map_err(::serde::de::Error::custom)?");
            w.dedent();
            w.line("),");
            w.line("ResponseTarget::Folder => RetrieveData::Folder(");
            w.indent();
            w.line("<crate::generated::verbs::retrieve::DataFolder as ::serde::Deserialize>::deserialize(payload).map_err(::serde::de::Error::custom)?");
            w.dedent();
            w.line("),");
            w.line("ResponseTarget::Scope => RetrieveData::Scope(");
            w.indent();
            w.line("<crate::generated::verbs::retrieve::DataScope as ::serde::Deserialize>::deserialize(payload).map_err(::serde::de::Error::custom)?");
            w.dedent();
            w.line("),");
            w.dedent();
            w.line("})");
            w.dedent();
            w.line("},");
        } else {
            w.line(&format!(
                "ResponseVerb::{pascal} => ResponseData::{pascal}("
            ));
            w.indent();
            w.line(&format!(
                "<crate::generated::verbs::{}::{pascal}Data as ::serde::Deserialize>::deserialize(payload).map_err(::serde::de::Error::custom)?",
                verb.id
            ));
            w.dedent();
            w.line("),");
        }
    }
    // The `unknown` sentinel verb only appears on rejected responses (per
    // the IDL UnknownVerb binding); the rejected path above already proved
    // data is None, so this match arm is unreachable. Reject defensively in
    // case the caller violates that invariant on the wire.
    w.line(
        "ResponseVerb::Unknown => return Err(::serde::de::Error::custom(\"verb=unknown is rejected-only and cannot carry data\")),",
    );
    w.dedent();
    w.line("})");
    w.dedent();
    w.line("} else { None };");
    w.line("Ok(Self {");
    w.indent();
    for field in &s.fields {
        let ident = sanitise_ident(&field.name);
        if field.name == "data" {
            w.line("data,");
        } else {
            w.line(&format!("{ident}: raw.{ident},"));
        }
    }
    w.dedent();
    w.line("})");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    Ok(())
}

// ── Preludes ─────────────────────────────────────────────────────────────────

fn emit_prelude(
    prelude: &PreludeDef,
    common_names: &BTreeSet<String>,
) -> Result<GeneratedFile, CodegenError> {
    let mut w = RustWriter::new();
    w.raw(HEADER);
    w.raw(RUSTFMT_SKIP);
    w.raw(FILE_LINTS);
    w.line(&format!(
        "//! Generated SDK type for the `{}` prelude.",
        prelude.id
    ));
    w.blank();
    w.line("use serde::{Deserialize, Serialize};");
    w.blank();
    let fallback = format!("{}Response", pascal_case(&prelude.id));
    write_named_type(&mut w, &prelude.response, &fallback, common_names)?;
    w.blank();
    let schema_path = format!(
        "../../../cairn-mcp/src/generated/schemas/prelude/{}.json",
        prelude.id
    );
    w.line(&format!(
        "pub const SCHEMA: &[u8] = include_bytes!(\"{schema_path}\");"
    ));
    Ok(GeneratedFile {
        path: PathBuf::from(ROOT).join(format!("{}.rs", prelude.id)),
        bytes: w.finish().into_bytes(),
    })
}

// ── Top-level type declaration writer ────────────────────────────────────────

/// Walk a `RustType` and emit a top-level Rust declaration. Nested types are
/// flattened into siblings so the file stays a flat module. (Recursive on
/// nested struct fields where the field type is itself a Struct/Enum/etc.)
fn write_type_decl(
    w: &mut RustWriter,
    ty: &RustType,
    common: &BTreeSet<String>,
) -> Result<(), CodegenError> {
    match ty {
        RustType::Struct(s) => write_struct(w, s, common),
        RustType::Enum(e) => {
            write_enum(w, e);
            Ok(())
        }
        RustType::TaggedUnion(t) => write_tagged_union(w, t, common),
        RustType::UntaggedUnion(u) => write_untagged_union(w, u, common),
        RustType::Recursive(r) => write_recursive(w, r, common),
        // For top-level emission, scalar variants get wrapped in a tiny
        // module-level newtype so they are addressable. In practice the IR
        // never lands here at the verb-file level — the verb's Args/Data is
        // always one of the declarable variants above.
        other => Err(CodegenError::Emit(format!(
            "write_type_decl: cannot emit non-named type at top level: {other:?}"
        ))),
    }
}

/// Like `write_type_decl`, but accepts an explicit fallback name to use when
/// the IR variant doesn't carry one (Json, Primitive, Vec, ...). Named
/// variants (Struct/Enum/...) are emitted as-is — the existing IR name wins
/// over the fallback.
fn write_named_type(
    w: &mut RustWriter,
    ty: &RustType,
    fallback: &str,
    common: &BTreeSet<String>,
) -> Result<(), CodegenError> {
    match ty {
        RustType::Struct(_)
        | RustType::Enum(_)
        | RustType::TaggedUnion(_)
        | RustType::UntaggedUnion(_)
        | RustType::Recursive(_) => write_type_decl(w, ty, common),
        RustType::Primitive(p) => {
            w.line("#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]");
            w.line("#[serde(transparent)]");
            w.line(&format!("pub struct {fallback}(pub {});", render_prim(*p)));
            Ok(())
        }
        RustType::Json => {
            w.line(&format!("pub type {fallback} = serde_json::Value;"));
            Ok(())
        }
        RustType::Vec(inner) => {
            w.line(&format!(
                "pub type {fallback} = Vec<{}>;",
                render_inline_type(inner, common)
            ));
            Ok(())
        }
        RustType::Map(inner) => {
            w.line(&format!(
                "pub type {fallback} = std::collections::BTreeMap<String, {}>;",
                render_inline_type(inner, common)
            ));
            Ok(())
        }
        RustType::Optional(inner) => {
            w.line(&format!(
                "pub type {fallback} = Option<{}>;",
                render_inline_type(inner, common)
            ));
            Ok(())
        }
        RustType::Ref(name) => {
            w.line(&format!(
                "pub type {fallback} = {};",
                render_ref(name, common)
            ));
            Ok(())
        }
    }
}

// ── Struct ───────────────────────────────────────────────────────────────────

fn write_struct(
    w: &mut RustWriter,
    s: &StructDef,
    common: &BTreeSet<String>,
) -> Result<(), CodegenError> {
    // Emit any nested structured types in the fields *first* so the parent
    // declaration's references resolve in source order.
    let mut nested = Vec::new();
    collect_nested_from_fields(&s.fields, &s.name.0, &mut nested);
    for (sub_name, sub_ty) in &nested {
        let renamed = renamed_to(sub_ty, TypeName::new(sub_name));
        write_type_decl(w, &renamed, common)?;
        w.blank();
    }

    if let Some(doc) = &s.doc {
        write_doc(w, doc);
    }
    // When the struct carries a presence-of-anyOf constraint OR is on the
    // bespoke-validators allow-list, derive only Serialize on the public type
    // and hand-roll Deserialize via a private Raw companion so the constraint
    // becomes a wire-level invariant.
    let needs_raw_companion = s.any_of_required.is_some() || struct_has_extra_validators(&s.name.0);
    if needs_raw_companion {
        w.line("#[derive(Debug, Clone, PartialEq, Serialize)]");
    } else {
        w.line("#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]");
    }
    if s.deny_unknown_fields {
        w.line("#[serde(deny_unknown_fields)]");
    }
    w.line(&format!("pub struct {} {{", s.name.0));
    w.indent();
    for field in &s.fields {
        write_field(w, field, &s.name.0, common, true);
    }
    w.dedent();
    w.line("}");

    if needs_raw_companion {
        w.blank();
        write_struct_raw_companion(w, s, common);
    }
    Ok(())
}

/// Bespoke-validator allow-list: structs whose Deserialize must run extra
/// IDL constraints the generic IR doesn't carry (numeric bounds, array
/// minItems, uniqueItems). Listed by Rust type name.
fn struct_has_extra_validators(name: &str) -> bool {
    matches!(name, "SearchArgs")
}

/// Emit the `Raw<Name>` companion + `TryFrom` + hand-rolled `Deserialize`
/// for a struct that needs anyOf-presence enforcement, type-specific extra
/// validators, or both. The shape mirrors the untagged-union path: a private
/// deserialise-only mirror, a `TryFrom` that runs every check, and a
/// `Deserialize` that funnels every parse through `TryFrom`.
fn write_struct_raw_companion(w: &mut RustWriter, s: &StructDef, common: &BTreeSet<String>) {
    let raw_name = format!("Raw{}", s.name.0);

    w.line("#[derive(Deserialize)]");
    if s.deny_unknown_fields {
        w.line("#[serde(deny_unknown_fields)]");
    }
    w.line(&format!("struct {raw_name} {{"));
    w.indent();
    for field in &s.fields {
        write_field_inner(w, field, &s.name.0, common, false, false);
    }
    w.dedent();
    w.line("}");
    w.blank();

    w.line(&format!(
        "impl ::core::convert::TryFrom<{raw_name}> for {} {{",
        s.name.0
    ));
    w.indent();
    w.line("type Error = &'static str;");
    w.line(&format!(
        "fn try_from(raw: {raw_name}) -> Result<Self, Self::Error> {{"
    ));
    w.indent();
    if let Some(any_of) = &s.any_of_required {
        let presence_checks = any_of
            .iter()
            .map(|n| format!("raw.{}.is_some()", sanitise_ident(n)))
            .collect::<Vec<_>>()
            .join(" || ");
        let names = any_of.join(", ");
        w.line(&format!(
            "if !({presence_checks}) {{ return Err(\"at least one of [{names}] is required\"); }}"
        ));
    }
    // Type-specific extra invariants. The IDL annotates ScopeFilter's string
    // fields with `minLength: 1` and array fields with `minItems: 1`; SearchArgs
    // bounds `limit` to [1, 1000]. The generic IR strips these, so without the
    // hook `{"tags": []}`, `{"user": ""}`, and `{"limit": 0}` slip through.
    if s.name.0 == "ScopeFilter" {
        write_scope_filter_extra_checks(w);
    }
    if s.name.0 == "SearchArgs" {
        write_search_args_extra_checks(w);
    }
    w.line("Ok(Self {");
    w.indent();
    for field in &s.fields {
        let ident = sanitise_ident(&field.name);
        w.line(&format!("{ident}: raw.{ident},"));
    }
    w.dedent();
    w.line("})");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();

    w.line(&format!(
        "impl<'de> ::serde::Deserialize<'de> for {} {{",
        s.name.0
    ));
    w.indent();
    w.line("fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>");
    w.line("where D: ::serde::Deserializer<'de> {");
    w.indent();
    w.line(&format!(
        "let raw = {raw_name}::deserialize(deserializer)?;"
    ));
    w.line("Self::try_from(raw).map_err(::serde::de::Error::custom)");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
}

fn write_field(
    w: &mut RustWriter,
    field: &StructField,
    parent_name: &str,
    common: &BTreeSet<String>,
    pub_qualifier: bool,
) {
    write_field_inner(w, field, parent_name, common, pub_qualifier, true);
}

/// Emit a struct field. `serializing` controls whether serialise-only attrs
/// (`skip_serializing_if`) are included — set to `false` when the host struct
/// derives only `Deserialize` (e.g. the private `Raw…` companion of an
/// untagged-union public type), so the generated code stays free of dead
/// attribute noise.
fn write_field_inner(
    w: &mut RustWriter,
    field: &StructField,
    parent_name: &str,
    common: &BTreeSet<String>,
    pub_qualifier: bool,
    serializing: bool,
) {
    if let Some(doc) = &field.doc {
        write_doc(w, doc);
    }
    let serde_name = if rust_keyword(&field.name) || !is_valid_ident(&field.name) {
        Some(field.name.clone())
    } else {
        None
    };
    if matches!(field.ty, RustType::Optional(_)) {
        if serializing {
            w.line("#[serde(default, skip_serializing_if = \"Option::is_none\")]");
        } else {
            w.line("#[serde(default)]");
        }
    }
    if let Some(name) = &serde_name {
        w.line(&format!("#[serde(rename = \"{name}\")]"));
    }
    let ident = sanitise_ident(&field.name);
    let ty_str = render_field_type(&field.ty, parent_name, &field.name, common);
    let prefix = if pub_qualifier { "pub " } else { "" };
    w.line(&format!("{prefix}{ident}: {ty_str},"));
}

// ── Enum (string enum) ───────────────────────────────────────────────────────

fn write_enum(w: &mut RustWriter, e: &EnumDef) {
    if let Some(doc) = &e.doc {
        write_doc(w, doc);
    }
    w.line("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]");
    if let Some(rename) = e.rename_all {
        w.line(&format!("#[serde(rename_all = \"{rename}\")]"));
    }
    w.line("#[non_exhaustive]");
    w.line(&format!("pub enum {} {{", e.name.0));
    w.indent();
    for variant in &e.variants {
        write_enum_variant(w, variant);
    }
    w.dedent();
    w.line("}");
    if e.variants.iter().any(|v| v.capability.is_some()) {
        w.blank();
        w.line(&format!("impl {} {{", e.name.0));
        w.indent();
        w.line("/// Capability advertised by the IDL for the matched variant, or `None`.");
        w.line("#[must_use]");
        w.line("pub const fn capability(self) -> Option<&'static str> {");
        w.indent();
        w.line("match self {");
        w.indent();
        for variant in &e.variants {
            match &variant.capability {
                Some(cap) => w.line(&format!("Self::{} => Some(\"{cap}\"),", variant.rust_ident)),
                None => w.line(&format!("Self::{} => None,", variant.rust_ident)),
            }
        }
        w.dedent();
        w.line("}");
        w.dedent();
        w.line("}");
        w.dedent();
        w.line("}");
    }
}

fn write_enum_variant(w: &mut RustWriter, variant: &EnumVariant) {
    if let Some(doc) = &variant.doc {
        write_doc(w, doc);
    }
    // Serde's rename_all handles the wire mapping for snake_case identifiers,
    // but if the wire form contains characters that don't fit the rename rule
    // we attach an explicit rename.
    if !wire_matches_rename(&variant.wire, &variant.rust_ident) {
        w.line(&format!("#[serde(rename = \"{}\")]", variant.wire));
    }
    w.line(&format!("{},", variant.rust_ident));
}

// ── Tagged union ─────────────────────────────────────────────────────────────

fn write_tagged_union(
    w: &mut RustWriter,
    t: &TaggedUnionDef,
    common: &BTreeSet<String>,
) -> Result<(), CodegenError> {
    // Hoist nested structured types referenced by variant fields.
    let mut nested = Vec::new();
    for variant in &t.variants {
        let parent = format!("{}{}", t.name.0, variant.rust_ident);
        collect_nested_from_fields(&variant.fields, &parent, &mut nested);
    }
    for (sub_name, sub_ty) in &nested {
        let renamed = renamed_to(sub_ty, TypeName::new(sub_name));
        write_type_decl(w, &renamed, common)?;
        w.blank();
    }

    if let Some(doc) = &t.doc {
        write_doc(w, doc);
    }
    let needs_raw_companion = tagged_union_has_extra_validators(&t.name.0);
    if needs_raw_companion {
        w.line("#[derive(Debug, Clone, PartialEq, Serialize)]");
    } else {
        w.line("#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]");
    }
    w.line(&format!(
        "#[serde(tag = \"{}\", rename_all = \"snake_case\")]",
        t.discriminator
    ));
    w.line("#[non_exhaustive]");
    w.line(&format!("pub enum {} {{", t.name.0));
    w.indent();
    for variant in &t.variants {
        write_tagged_variant(w, variant, &t.name.0, common);
    }
    w.dedent();
    w.line("}");
    if needs_raw_companion {
        w.blank();
        write_tagged_union_raw_companion(w, t, common);
    }
    if t.variants.iter().any(|v| v.capability.is_some()) {
        w.blank();
        w.line(&format!("impl {} {{", t.name.0));
        w.indent();
        w.line("/// Capability advertised by the IDL for the matched variant, or `None`.");
        w.line("#[must_use]");
        w.line("pub fn capability(&self) -> Option<&'static str> {");
        w.indent();
        w.line("match self {");
        w.indent();
        for variant in &t.variants {
            // Match arm pattern depends on whether the variant has fields.
            let arm = if variant.fields.is_empty() {
                format!("Self::{}", variant.rust_ident)
            } else {
                format!("Self::{} {{ .. }}", variant.rust_ident)
            };
            match &variant.capability {
                Some(cap) => w.line(&format!("{arm} => Some(\"{cap}\"),")),
                None => w.line(&format!("{arm} => None,")),
            }
        }
        w.dedent();
        w.line("}");
        w.dedent();
        w.line("}");
        w.dedent();
        w.line("}");
    }
    Ok(())
}

/// Bespoke-validator allow-list for tagged unions: enums whose variants need
/// extra IDL constraints (numeric bounds, array minItems, uniqueItems, or
/// per-variant `additionalProperties: false`) the generic IR doesn't carry.
/// Listed by Rust type name.
///
/// Routing a tagged union through the Raw-companion path lets us emit
/// `#[serde(deny_unknown_fields)]` per Raw struct variant — without it,
/// serde's internally-tagged dispatch silently drops cross-variant fields
/// (e.g. a `scope` payload smuggled into a `target=record` retrieve call).
fn tagged_union_has_extra_validators(name: &str) -> bool {
    matches!(name, "RetrieveArgs" | "ForgetArgs")
}

/// Emit the `Raw<Name>` companion + `TryFrom` + hand-rolled `Deserialize` for
/// a tagged union with type-specific extra validators (currently RetrieveArgs).
/// The Raw mirror reuses serde's tagged-enum dispatch, then per-variant TryFrom
/// arms enforce the IDL constraints (limit ranges, include uniqueItems, etc.).
fn write_tagged_union_raw_companion(
    w: &mut RustWriter,
    t: &TaggedUnionDef,
    common: &BTreeSet<String>,
) {
    let raw_name = format!("Raw{}", t.name.0);

    // Per-variant Raw structs each carry `#[serde(deny_unknown_fields)]` so
    // payloads with cross-variant or otherwise-unknown keys reject at parse
    // time rather than silently dropping the offending field. The IDL marks
    // every Args variant `additionalProperties: false` — without these structs
    // the serde tagged-enum dispatch would accept (and drop) e.g. a `scope`
    // field smuggled into a `target=record` retrieve call. We can't put
    // `deny_unknown_fields` on enum variants directly (serde rejects it), so
    // each variant lowers to a sibling struct and the Raw enum itself peeks
    // the discriminator from a `serde_json::Value` and dispatches.
    for variant in &t.variants {
        if variant.fields.is_empty() {
            continue;
        }
        let raw_variant_name = format!("{raw_name}{}", variant.rust_ident);
        w.line("#[derive(Deserialize)]");
        w.line("#[serde(deny_unknown_fields)]");
        w.line(&format!("struct {raw_variant_name} {{"));
        w.indent();
        let nested_parent = format!("{}{}", t.name.0, variant.rust_ident);
        // Discriminator field is deserialised but ignored — its value was
        // already matched in the dispatcher below.
        w.line(&format!(
            "#[allow(dead_code)] {}: serde::de::IgnoredAny,",
            sanitise_ident(&t.discriminator)
        ));
        for field in &variant.fields {
            write_field_inner(w, field, &nested_parent, common, false, false);
        }
        w.dedent();
        w.line("}");
        w.blank();
    }

    // Raw enum: variants keyed by discriminator, each carrying its typed
    // (deny_unknown_fields-marked) raw struct. The hand-rolled Deserialize
    // peeks the discriminator from a `serde_json::Value`, then re-deserialises
    // the whole payload as the matched per-variant struct.
    w.line(&format!("enum {raw_name} {{"));
    w.indent();
    for variant in &t.variants {
        if variant.fields.is_empty() {
            w.line(&format!("{},", variant.rust_ident));
            continue;
        }
        let raw_variant_name = format!("{raw_name}{}", variant.rust_ident);
        w.line(&format!("{}({raw_variant_name}),", variant.rust_ident));
    }
    w.dedent();
    w.line("}");
    w.blank();

    w.line(&format!(
        "impl<'de> ::serde::Deserialize<'de> for {raw_name} {{"
    ));
    w.indent();
    w.line("fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>");
    w.line("where D: ::serde::Deserializer<'de> {");
    w.indent();
    w.line("let value = ::serde_json::Value::deserialize(deserializer)?;");
    w.line(&format!(
        "let tag = value.get(\"{}\").and_then(::serde_json::Value::as_str).ok_or_else(|| ::serde::de::Error::custom(\"missing discriminator: {}\"))?;",
        t.discriminator, t.discriminator
    ));
    w.line("match tag {");
    w.indent();
    for variant in &t.variants {
        if variant.fields.is_empty() {
            w.line(&format!(
                "\"{}\" => Ok(Self::{}),",
                variant.wire, variant.rust_ident
            ));
            continue;
        }
        let raw_variant_name = format!("{raw_name}{}", variant.rust_ident);
        w.line(&format!("\"{}\" => {{", variant.wire));
        w.indent();
        w.line(&format!(
            "let v = <{raw_variant_name} as ::serde::Deserialize>::deserialize(value).map_err(::serde::de::Error::custom)?;"
        ));
        w.line(&format!("Ok(Self::{}(v))", variant.rust_ident));
        w.dedent();
        w.line("},");
    }
    w.line(&format!(
        "other => Err(::serde::de::Error::custom(format!(\"unknown {} value: {{other}}\")))",
        t.discriminator
    ));
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();

    // TryFrom: per-variant constraint enforcement, then convert to public.
    // Raw variants are tuple-form `Variant(RawNameVariant)` — we destructure
    // the inner per-variant struct and bind each field for the IDL checks
    // before materialising the public struct-style enum variant.
    w.line(&format!(
        "impl ::core::convert::TryFrom<{raw_name}> for {} {{",
        t.name.0
    ));
    w.indent();
    w.line("type Error = &'static str;");
    w.line(&format!(
        "fn try_from(raw: {raw_name}) -> Result<Self, Self::Error> {{"
    ));
    w.indent();
    w.line("match raw {");
    w.indent();
    for variant in &t.variants {
        let dest = &variant.rust_ident;
        if variant.fields.is_empty() {
            w.line(&format!("{raw_name}::{dest} => Ok(Self::{dest}),"));
            continue;
        }
        let bind: Vec<String> = variant
            .fields
            .iter()
            .map(|f| sanitise_ident(&f.name))
            .collect();
        let bind_list = bind.join(", ");
        w.line(&format!("{raw_name}::{dest}(inner) => {{"));
        w.indent();
        // Move every typed field out of the per-variant Raw struct and bind it
        // by its public-variant ident so the per-variant IDL checks below
        // (limit ranges, uniqueItems, …) can refer to bare names.
        for ident in &bind {
            w.line(&format!("let {ident} = inner.{ident};"));
        }
        // Type-specific per-variant checks for RetrieveArgs / ForgetArgs.
        if t.name.0 == "RetrieveArgs" {
            write_retrieve_args_variant_checks(w, &variant.rust_ident);
        } else if t.name.0 == "ForgetArgs" {
            write_forget_args_variant_checks(w, &variant.rust_ident);
        }
        w.line(&format!("Ok(Self::{dest} {{ {bind_list} }})"));
        w.dedent();
        w.line("},");
    }
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
    w.blank();

    w.line(&format!(
        "impl<'de> ::serde::Deserialize<'de> for {} {{",
        t.name.0
    ));
    w.indent();
    w.line("fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>");
    w.line("where D: ::serde::Deserializer<'de> {");
    w.indent();
    w.line(&format!(
        "let raw = {raw_name}::deserialize(deserializer)?;"
    ));
    w.line("Self::try_from(raw).map_err(::serde::de::Error::custom)");
    w.dedent();
    w.line("}");
    w.dedent();
    w.line("}");
}

/// Per-variant validators for `RetrieveArgs`, lifted from `verbs/retrieve.json`.
/// Local bindings (`session_id`, `limit`, `include`, ...) come from the TryFrom
/// match arm above. Nested types (Vec<RetrieveArgsSessionInclude> etc.) live
/// in the same module so we can call methods on them directly.
fn write_retrieve_args_variant_checks(w: &mut RustWriter, variant: &str) {
    match variant {
        "Record" => {
            // id is a Ulid (newtype) — shape already enforced if present.
        }
        "Session" => {
            w.line("if session_id.is_empty() { return Err(\"session_id: must not be empty\"); }");
            w.line("if let Some(lim) = limit {");
            w.indent();
            w.line(
                "if !(1..=10000).contains(&lim) { return Err(\"limit: must be in [1, 10000]\"); }",
            );
            w.dedent();
            w.line("}");
            w.line("if let Some(inc) = &include {");
            w.indent();
            w.line(
                "if inc.is_empty() { return Err(\"include: must contain at least one item\"); }",
            );
            // uniqueItems
            w.line("let mut seen = ::std::collections::BTreeSet::new();");
            w.line("for item in inc {");
            w.indent();
            w.line(
                "if !seen.insert(*item as u8) { return Err(\"include: items must be unique\"); }",
            );
            w.dedent();
            w.line("}");
            w.dedent();
            w.line("}");
        }
        "Turn" => {
            w.line("if session_id.is_empty() { return Err(\"session_id: must not be empty\"); }");
            w.line("if let Some(inc) = &include {");
            w.indent();
            w.line(
                "if inc.is_empty() { return Err(\"include: must contain at least one item\"); }",
            );
            w.line("let mut seen = ::std::collections::BTreeSet::new();");
            w.line("for item in inc {");
            w.indent();
            w.line(
                "if !seen.insert(*item as u8) { return Err(\"include: items must be unique\"); }",
            );
            w.dedent();
            w.line("}");
            w.dedent();
            w.line("}");
        }
        "Folder" => {
            w.line("if path.is_empty() { return Err(\"path: must not be empty\"); }");
            w.line("if let Some(d) = depth {");
            w.indent();
            w.line("if d > 16 { return Err(\"depth: must be in [0, 16]\"); }");
            w.dedent();
            w.line("}");
        }
        "Scope" => {
            w.line("if let Some(c) = &cursor {");
            w.indent();
            w.line("if c.is_empty() { return Err(\"cursor: must not be empty\"); }");
            w.line("if c.len() > 512 { return Err(\"cursor: must be <= 512 chars\"); }");
            w.dedent();
            w.line("}");
        }
        "Profile" => {
            // anyOf [user, agent] required — IR currently doesn't enforce this
            // at the variant level. Defend it here.
            w.line("if user.is_none() && agent.is_none() { return Err(\"at least one of [user, agent] is required\"); }");
            w.line("if let Some(u) = &user {");
            w.indent();
            w.line("if u.is_empty() { return Err(\"user: must not be empty\"); }");
            w.dedent();
            w.line("}");
            w.line("if let Some(a) = &agent {");
            w.indent();
            w.line("if a.is_empty() { return Err(\"agent: must not be empty\"); }");
            w.dedent();
            w.line("}");
        }
        _ => {}
    }
}

